        .section .rodata            // Секция read-only, часть секции .text
str:    .string "Hello, world!\n"   // str - метка на область памяти, .string - 0-terminated строка
        .set str_len, . - str - 1   // Определить символ str_len, содержащий размер строки (кроме \0)

        .text                       // Секция .text, права readonly + exec
        .global main                // Экспортируем точку входа в программу, используется _start
main:                               // Метка на область памяти с кодом main
        movl    $4, %eax            // Положить код системного вызова 4 (write) в регистр %eax
        movl    $1, %ebx            // Положить дескриптор потока 1 (stdin) в регистр %ebx
        movl    $str, %ecx          // Положить адрес области памяти со строкой в регистр %ecx
        movl    $str_len, %edx           // Положить длину строки в регистр %edx
        int     $0x80               // Прерывание 0x80

        movl    $1, %eax            // Положить код системного вызова 1 (exit) в регистр %eax
        movl    $0, %ebx            // Положить значение кода возврата в регистр %ebx
        int     $0x80               // Прерывание 0x80
